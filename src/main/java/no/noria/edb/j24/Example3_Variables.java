package no.noria.edb.j24;

public class Example3_Variables {
    public static void main(String[] args) {

        // A variable called *myInt* with the type int (integer / whole number) and value 30.
        int myInt = 30;
        System.out.println("My int is " + myInt);

        // A variable called *myString* with the type String (text, alphanumeric and symbols) and value "Hello!".
        String myString = "Hello!";
        System.out.println("My int is " + myString);
    }
}
